#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *p1;

	p1 = s1;
	while (*p1)
		p1++;
	while (n > 0 && *s2)
	{
		*p1++ = *s2++;
		n--;
	}
	*p1 = '\0';
	return (s1);
}
