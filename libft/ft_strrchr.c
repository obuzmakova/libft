#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *p1;

	p1 = 0;
	while (*s)
	{
		if (*s == (char)c)
			p1 = (char*)s;
		s++;
	}
	if ((char)c == '\0')
		return ((char*)s);
	return (p1);
}
